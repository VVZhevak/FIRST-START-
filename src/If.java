public class If {
    public static void main(String[] args) {
        int myint=5;
        if (myint<10) {
          System.out.println("да, верно, все отлично");
        } else if (myint>20) {
            System.out.println("нет, не верно");
        } else
            System.out.println("не попадает ни один из перечисленных");
    }


}
